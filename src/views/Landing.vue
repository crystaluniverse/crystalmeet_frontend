<template>
  <v-row align="center" justify="center" class="landing">
    <v-col cols="8">
      <v-card>
        <v-card-title>
          <p class="text-center" style="width:100%">FreeFlowConnect</p>
        </v-card-title>
        <v-card-text>
          <v-row align="center">
            <v-col align="center">
              <v-form @submit.prevent="joinRoom" v-model="valid">
                <v-text-field
                  filled
                  label="Invite url or room ID"
                  persistent-hint
                  v-model="inviteUrl"
                  hint="Paste the url or room ID you've received"
                  :rules="inviteUrlRules"
                  required
                >
                  <template v-slot:append>
                    <v-btn :disabled="!valid" small text type="submit">Join room</v-btn>
                  </template>
                </v-text-field>
              </v-form>
            </v-col>
            <v-divider vertical></v-divider>
            <v-col align="center">
              <v-btn text @click="create">Create room</v-btn>
            </v-col>
          </v-row>
        </v-card-text>
      </v-card>
    </v-col>
  </v-row>
</template>
<script>
import { mapActions, mapGetters } from "vuex";
export default {
  data() {
    return {
      /* eslint-disable */
      reg: new RegExp("(?:https://.*/room/)?(.*)"),
      /* eslint-enable */
      valid: false,
      inviteUrlRules: [
        url => !!url || "Invite url is required",
        url => this.reg.test(url) || "Invite url or room ID  invalid"
      ],
      inviteUrl: null
    };
  },
  computed: {
    ...mapGetters(["teamName"])
  },
  methods: {
    ...mapActions(["createTeam"]),
    create() {
      this.createTeam();
    },
    joinRoom() {
      if (this.inviteUrl && this.reg.test(this.inviteUrl)) {
        this.$router.push({
          name: "room",
          params: {
            token: this.inviteUrl.match(this.reg)[1].substring(0,15)
          }
        });
      }
    }
  },
  watch: {
    inviteUrl(val) {
      if (val && this.reg.test(val) && val.length > 15) {
        this.inviteUrl = val.match(this.reg)[1];
      }
    },
    teamName(val) {
      if (val) {
        this.$router.push({ name: "room", params: { token: val } });
      }
    }
  }
};
</script>
